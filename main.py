def find_max(A):
    max_val = A[0]
    max_idx = 0
    i = 1

    while i < len(A):
        if A[i] >= max_val:
            max_val = A[i]
            max_idx = i
        i += 1

    return max_val, max_idx
print(*find_max([1, 2, 1, 2, 1]))



